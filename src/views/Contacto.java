    private void EnviarMensajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarMensajeActionPerformed
        java.util.ResourceBundle bundle = java.util.ResourceBundle.getBundle("resources/Bundle_es_ES");

        String email = txtEmail.getText().trim();
        String asunto = txtAsunto.getText().trim();
        String mensaje = txtMensaje.getText().trim();

        if (mensaje.isEmpty() || email.equals(bundle.getString("CONTACTO_PLACEHOLDER_EMAIL")) || asunto.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    bundle.getString("CONTACTO_ERR_MSG"),
                    bundle.getString("CONTACTO_ERR_TIT"),
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        String regexEmail = "^[A-Za-z0-9+_.-]+@(.+)$";
        if (!email.matches(regexEmail)) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "<html><b style='color: #C0392B;'>Error de formato:</b><br>Por favor, introduzca una dirección de correo válida.</html>",
                    bundle.getString("CONTACTO_ERR_TIT"),
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }


        javax.swing.JOptionPane.showMessageDialog(this,
                bundle.getString("CONTACTO_OK_MSG"),
                bundle.getString("CONTACTO_OK_TIT"),
                javax.swing.JOptionPane.INFORMATION_MESSAGE);

        txtMensaje.setText("");
        txtAsunto.setText("");
        txtEmail.setText(bundle.getString("CONTACTO_PLACEHOLDER_EMAIL"));
    }//GEN-LAST:event_EnviarMensajeActionPerformed

